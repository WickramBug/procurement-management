    public void approvalTableLoad() {

        rs = poc.getPurchaseOrderDetails();

        DefaultTableModel tableModel = (DefaultTableModel) DbUtils.resultSetToTableModel(rs);
        tableModel.addColumn("VIEW");
        tableModel.addColumn("DELETE");
        tableModel.addColumn("APPROVE");

        tableRequsition.setModel(tableModel);
        tableRequsition.getColumn("VIEW").setCellRenderer(new ButtonRenderer("VIEW"));
        tableRequsition.getColumn("DELETE").setCellRenderer(new ButtonRenderer("DECLINE"));
        tableRequsition.getColumn("APPROVE").setCellRenderer(new ButtonRenderer("APPROVE"));
        setTableHeaderColor();

    }

