    private void tableRequsitionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRequsitionMouseClicked
        // TODO add your handling code here:
        int row = tableRequsition.rowAtPoint(evt.getPoint());
        int col = tableRequsition.columnAtPoint(evt.getPoint());
        
        if (col == 3) {
                openFile(tableRequsition.getValueAt(row, 0).toString());
            
        } else if (col == 4) {
            
                int response = JOptionPane.showConfirmDialog(null, "Do you really want to Decline the selected Purchase Order");
                if (response == 0) {
                    prc.UpdateRequisitionStatus("Declined",poc.getRequisitionNo(Integer.parseInt(tableRequsition.getValueAt(row, 0).toString().substring(5))));
                    poc.UpdatePurchaseOrderStatus("Declined", Integer.parseInt(tableRequsition.getValueAt(row, 0).toString().substring(5)));
                    approvalTableLoad();
                    
                }
        }else if (col == 5) {
                                      
                    prc.UpdateRequisitionStatus("Approved",poc.getRequisitionNo(Integer.parseInt(tableRequsition.getValueAt(row, 0).toString().substring(5))));
                    poc.UpdatePurchaseOrderStatus("Approved", Integer.parseInt(tableRequsition.getValueAt(row, 0).toString().substring(5)));
                    SendMail sm=new SendMail();
                    sm.sendPOMail(tableRequsition.getValueAt(row, 0).toString());
                    JOptionPane.showMessageDialog(null, "Purchase Order is Sent to the Supplier");
                    approvalTableLoad();            
                
        }
    }//GEN-LAST:event_tableRequsitionMouseClicked

