    private void calculateTotal() {

        double total = 0;
        for (int i = 0; i < tableRequsition.getRowCount(); i++) {
            try {
                total += Double.parseDouble(tableRequsition.getValueAt(i, 4).toString());
                //  items.put(id, quantity);

            } catch (NumberFormatException ex) {

                JOptionPane.showMessageDialog(null, " Please Insert a valid quantity for Item");
                // items.clear();
                // return false;
            } catch (NullPointerException ex) {

                JOptionPane.showMessageDialog(null, " Please Insert a quantity for Item");
                //  items.clear();
                //  return false;
            }
            lblTotal.setText(Double.toString(total));
            lblOrderAmnt.setText(Double.toString(total - deductions()));

        }
    }

