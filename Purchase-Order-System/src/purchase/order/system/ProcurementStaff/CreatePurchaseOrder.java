    private boolean checkPrice() {

        int id;
        double price;
        for (int i = 0; i < tableRequsition.getRowCount(); i++) {

            id = Integer.parseInt(tableRequsition.getValueAt(i, 0).toString().substring(3));
            if (Double.parseDouble(tableRequsition.getValueAt(i, 3).toString()) > 0) {
                try {
                    price = Double.parseDouble(tableRequsition.getValueAt(i, 3).toString());
                    itemPrice.put(id, price);
                } catch (NumberFormatException ex) {

                    JOptionPane.showMessageDialog(null, " Please Insert a valid Price for Item" + id);
                    itemPrice.clear();
                    return false;
                } catch (NullPointerException ex) {

                    JOptionPane.showMessageDialog(null, " Please Insert a Price for Item" + id);
                    itemPrice.clear();
                    return false;
                }
            } else {
                //  System.out.println(jTable2.getValueAt(i, 1).toString().trim().length());
                JOptionPane.showMessageDialog(null, " Please Insert a Price for Item" + id);
                itemPrice.clear();
                return false;
            }
        }

        return true;
    }
